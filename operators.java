public class operators {
    public static void main(String[] args) {
        byte var1=21;
        short var2=25;
        System.out.println(var1 + var2);
        System.out.println(var1 - var2);
        System.out.println(var1 * var2);
        System.out.println(var1 / var2);
        System.out.println(var1 % var2);
        System.out.println(var1 > var2);
        System.out.println(var1 >= var2);
        System.out.println(var1 < var2);
        System.out.println(var1 <= var2);
        //System.out.println(var1 <> var2);
        System.out.println(var1 != var2);
        System.out.println(var1 << var2);
        System.out.println(var1 >>> var2);
        //System.out.println(var1 && var2);
        //System.out.println(var1 || var2);
        System.out.println(var1 & var2);
        System.out.println(var1 | var2);
    }
}
